        private byte m_Status = 0;
        public byte Status
        {
            get { return m_Status; }
            set { m_Status = value; }
        }
        public bool Indeterminate
        {
            get { return ((m_Status & 0x20) == 0x20); }
            set
            {
                if (value == true)
                {
                    m_Status = (byte)(m_Status | 0x20);
                }
                else
                {
                    m_Status = (byte)(m_Status & 0xDF);
                }
            }
        }
        public bool Curse
        {
            get { return ((m_Status & 0x40)  == 0x40); }
            set
            {
                if (value == true)
                {
                    m_Status = (byte)(m_Status | 0x40);
                }
                else
                {
                    m_Status = (byte)(m_Status & 0xBF);
                }
            }
        }
        public bool Equipment
        {
            get { return ((m_Status & 0x80 ) == 0x80); }
            set
            {
                if (value == true)
                {
                    m_Status = (byte)(m_Status | 0x80);
                }
                else
                {
                    m_Status = (byte)(m_Status & 0x7F);
                }
            }
        }
        public WizItem(WIZSCN scn = WIZSCN.FC1)
        {
            m_scn = scn;
        }
        public string ItemName
        {
            get
            {
                switch (m_scn)
                {
                    case WIZSCN.FC1:
                    case WIZSCN.SFC1:
                        return m_WizFCItemNames1[m_ID];
                    case WIZSCN.FC2:
                    case WIZSCN.SFC2:
                        return m_WizFCItemNames2[m_ID];
                    case WIZSCN.FC3:
                    case WIZSCN.SFC3:
                        return m_WizFCItemNames3[m_ID];
                    case WIZSCN.SFC5:
                        return m_WizSFCItemNames5[m_ID];
                    default:
                        return "";
                }

            }
        }
        public string CaptionText
        {
            get
            {
                string s = "";
                if(Indeterminate == true)
                {
                    s = ItemName.Substring(0, 3);
                    s += "未識別";
                }
                else
                {
                    s = ItemName;
                }
                return s;
            }
        }
        public string [] ItemNames
        {
            get
            {
                switch (m_scn)
